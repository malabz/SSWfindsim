link_list *new_node()
{
	link_list* new_n = (link_list *)malloc(sizeof(link_list));
	if (new_n == NULL) { fprintf(stderr, "Error: Can not alloc link list. Program will exit\n"); exit(1); }
	new_n -> nxt = NULL;
	new_n -> s = NULL;
	return new_n;
}

void free_node(link_list* this_node)
{
	link_list* free_node_ = this_node, *free_nxt;
	while (free_node_) 
	{
		if (free_node_->s) free(free_node_->s);
		free_nxt = free_node_ -> nxt;
		free(free_node_);
		free_node_ = free_nxt;
	}
}

