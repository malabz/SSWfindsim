/* This function is from MAFFT */

double countATGC(char* s)
{
	int nATGC;
	int nChar;
	char c;
	nATGC = nChar = 0;

	do
	{
		c = tolower(*s);
		if (isalpha(c))
		{
			nChar++;
			if (c == 'a' || c == 't' || c == 'g' || c == 'c' || c == 'u' || c == 'n')
				nATGC++;
		}
	} while (*++s);
	return((double)nATGC / nChar);
}

void alignmentout(s_align* align, FILE *filename, int number)
{
	if (align->ref_begin1 == -1 || align->read_begin1 == -1 || align->cigarLen == 0)
	{
		fprintf(stderr, "Warning: not found in sequence %d\n", number);
		return;
	}
	fprintf(filename, "%d %d %d %d %d %d ", number, align->ref_begin1, align->ref_end1, align->read_begin1, align->read_end1, align->score1);
	if (align->cigar) 
	{
		int32_t c = 0;
		for (; c < align->cigarLen; ++c)
		{
			fprintf(filename, "%d", cigar_int_to_len(align->cigar[c]));
			fputc(cigar_int_to_op(align->cigar[c]), filename);
		}
	}
	fputc('\n', filename);
}